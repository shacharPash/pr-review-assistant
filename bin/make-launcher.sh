#!/usr/bin/env bash
set -euo pipefail

# Generates a double-clickable "PR Review Assistant.command" launcher.
# Double-clicking it opens Terminal and runs `npm run serve` from the repo —
# i.e. the server runs in your interactive session, so Claude's login refreshes
# automatically (a headless background agent can't do that for org-managed
# Claude). Drag the file into your Dock for one-click starts.

REPO="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
# Capture node's bin dir now (nvm lives in .zshrc, which a double-clicked
# .command won't source) so npm is always found.
NODE_BIN_DIR="$(dirname "$(command -v node)")"
DEST="${LAUNCHER_DEST:-$HOME/Desktop/PR Review Assistant.command}"

cat > "$DEST" <<EOF
#!/bin/zsh
# PR Review Assistant — double-click to start the local server.
# Generated by 'npm run make-launcher'; re-run that to regenerate.
export PATH="$NODE_BIN_DIR:\$PATH"
cd "$REPO" || { echo "Repo not found: $REPO"; exit 1; }
echo "Starting PR Review Assistant…  (press Ctrl+C, or close this window, to stop)"
echo
exec npm run serve
EOF

chmod +x "$DEST"

echo "✅ Created: $DEST"
echo "   • Drag it into your Dock (right side), then double-click to start the server."
echo "   • Leave the Terminal window open while you review; close it to stop."
